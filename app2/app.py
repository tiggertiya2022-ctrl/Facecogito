@app.route("/api/quote", methods=["GET"])
def get_quote():
    category = request.args.get('category', 'กำลังใจ')
    if category in quotes:
        import random
        quote = random.choice(quotes[category])
        print('ok')
        return jsonify({"quote": quote, "category": category})
    else:
        return jsonify({"quote": "ไม่พบหมวดหมู่คำคมที่คุณค้นหา", "category": category})

